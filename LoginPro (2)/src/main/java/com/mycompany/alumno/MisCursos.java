    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int indiceSeleccionado = jTable2.getSelectedRow();
        Alumno alumnoSeleccionado = alumnoLogeado;
        Curso cursoSeleccionado = null;
        if (indiceSeleccionado >=0) {
            int idCurso = Integer.parseInt(jTable2.getValueAt(indiceSeleccionado, 1).toString().split("-")[0].trim());
            for (Curso curso : LoginPro.cursos) {
                if (curso.getIdCurso() == idCurso ) {
                    cursoSeleccionado = curso;
                    break;
                }
            }
            if (alumnoSeleccionado != null && cursoSeleccionado != null) {
                AsignaEstudiante ae = new AsignaEstudiante();
                String carne = alumnoSeleccionado.carne;
                String respuestaAsignacion = ae.AsignarEstudianteACurso(carne, idCurso);
                JOptionPane.showMessageDialog(this, respuestaAsignacion);
                cargarCursos(alumnoLogeado.carne);
            }
        }else{
            JOptionPane.showMessageDialog(null, "No ha seleccionado ningun curso");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

