    public void cargarCursos(String carne){
        listaCursosAsignados.clear();
        listaCursosNoAsignados.clear();
        boolean setCurso = false;
        for (Curso curso : LoginPro.cursos) { //programacion, matematica
            setCurso = false;
            for (Alumno a : curso.getAlumnosCurso()) { //Recorriendo los alumnos que tiene matematica
                if (a.carne.equals(carne)) { //carne 2 -> 1
                    listaCursosAsignados.add(curso);
                    setCurso = true;
                    break;
                }
            }
            if (!setCurso) {   
                listaCursosNoAsignados.add(curso);
            }
        }
        
        if (!listaCursosAsignados.isEmpty()) { //true
            DefaultTableModel model = new DefaultTableModel(
            new String[]{"#","Nombre Curso","Seccion","Fecha Inicio", "Fecha Fin", "Profesor", "Nota"},
                listaCursosAsignados.size());
                jTable1.setModel(model);
                int row = 0;
            for (Curso c : listaCursosAsignados) {
                jTable1.setValueAt(row+1, row, 0);
                jTable1.setValueAt(c.getIdCurso()+"-"+ c.getNombre(), row, 1);
                jTable1.setValueAt(c.getSeccion(), row, 2);
                jTable1.setValueAt(c.getFechaInicio(), row, 3);
                jTable1.setValueAt(c.getFechaFin(), row, 4);
                jTable1.setValueAt(c.getNombreProfesor(), row, 5);
                for (Alumno l : c.alumnosCurso) {
                    if (l.carne.equals(alumnoLogeado.carne)) {
                        jTable1.setValueAt(l.nota, row, 6);
                    }
                }
                row++;
            }
        }else{
            DefaultTableModel model = new DefaultTableModel(
            new String[]{"#","Nombre Curso","Seccion","Fecha Inicio", "Fecha Fin", "Profesor"},
                listaCursosAsignados.size());
            jTable1.setModel(model);
        }
        
        if (!listaCursosNoAsignados.isEmpty()) {
            DefaultTableModel model = new DefaultTableModel(
            new String[]{"#","Nombre Curso","Seccion","Fecha Inicio", "Fecha Fin","Profesor"},
                listaCursosNoAsignados.size());
            jTable2.setModel(model);
            int row = 0;
            for (Curso c : listaCursosNoAsignados) {
                jTable2.setValueAt(row+1, row, 0);
                jTable2.setValueAt(c.getIdCurso()+"-"+c.getNombre(), row, 1);
                jTable2.setValueAt(c.getSeccion(), row, 2);
                jTable2.setValueAt(c.getFechaInicio(), row, 3);
                jTable2.setValueAt(c.getFechaFin(), row, 4);
                jTable2.setValueAt(c.getNombreProfesor(), row, 5);
                row++;
            }
        }else{
            DefaultTableModel model = new DefaultTableModel(
            new String[]{"#","Nombre Curso","Seccion","Fecha Inicio", "Fecha Fin", "Profesor"},
                listaCursosNoAsignados.size());
            jTable2.setModel(model);
        }
    }
    
