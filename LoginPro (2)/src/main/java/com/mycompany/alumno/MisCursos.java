    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int indiceSeleccionado = jTable1.getSelectedRow();
        if (indiceSeleccionado >= 0) {
            int idCurso = Integer.parseInt(jTable1.getValueAt(indiceSeleccionado, 1).toString().split("-")[0].trim());
            for (Curso curso : LoginPro.cursos) {
                if (curso.getIdCurso() == idCurso) {
                    curso.eliminarAlumno(alumnoLogeado.carne);
                    break;
                }
            }
            cargarCursos(alumnoLogeado.carne);
            for (Curso curso : LoginPro.cursos) {
                System.out.println("Estos son los alumnos ");
                if (curso.getIdCurso() == idCurso) {
                    for (Alumno alumno : curso.alumnosCurso) {
                        System.out.println(alumno.nombre+" "+alumno.nota);
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "No ha seleccionado ningun curso");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

